public class Variables {
	public static void main(String[] args){
		int x; // declaration
		float z = 3.14f;
		double y = 120.34;
		boolean t = false;
		char symbol = '@';
		String name = "Kien";
		x = 123; 
		System.out.println("My integer number is: "+x);
		System.out.println("My float number is: "+z);
		System.out.println("My double number is: "+y);
		System.out.println(t); 
		System.out.println(symbol);
		System.out.println("Hello "+name);	
	}
}
