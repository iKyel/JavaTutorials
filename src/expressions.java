
public class expressions {

	public static void main(String[] args) {
		// expression = operands 
		// operands = values, variables, numbers, quantity
		// operators = + - * / %
		int friends = 10;
		System.out.println("friends = "+friends);
		friends = friends + 1;
		System.out.println("friends = "+friends);
		friends ++;
		System.out.println("friends = "+friends);
		friends --;
		System.out.println("friends = "+friends);
		friends /= 2;
		System.out.println("friends = "+friends);
		friends %= 3;
		System.out.println("friends = "+friends);
		
		double x = 10;
		x = (double) x/3;
		System.out.println("x = "+x);
		
		
	}

}
